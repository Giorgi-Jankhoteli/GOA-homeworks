num1 = int(input("Enter Number: "))
num2 = int(input("Enter Number: "))

def plus(num1, num2):
    return num1 + num2

def mult(num1, num2):
    return num1 * num2

def div(num1, num2):
    return num1 // num2

def min(num1, num2):
    return num1 - num2

print(plus(num1, num2))
print(mult(num1, num2))
print(div(num1, num2))
print(min(num1, num2))